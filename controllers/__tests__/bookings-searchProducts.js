describe('Bookings Product Search Lock Mechanism (Job Queuing on Stale Cache)', () => {
  let doApiPost;
  let lockTestPlugin; // Specific plugin instance for this suite
  let userToken;
  let testAppName;
  let testUserId;
  let ttrTestHint;
  const shortTTRTokenConfig = {
    endpoint: 'https://api.travelgatex.com/lock-test',
    apiKey: chance.guid(),
    client: 'tourconnect-lock-test',
    ttlForProducts: 1, // 1 second TTR for faster testing
  };

  beforeAll(async () => {
    doApiPost = globalDoApiPost;
    lockTestPlugin = globalPlugins.find(p => p.name === 'lockTestPlugin');
    if (!lockTestPlugin) {
      throw new Error("lockTestPlugin not found. Ensure it's included in global testUtils setup.");
    }

    ttrTestHint = 'lock-mechanism-hint';

    // Use globalUtils.appSetup to create the app, user, integration, and userAppKey
    // This ensures the Integration 'lockTestPlugin' is created before UserAppKey.
    // The appName here must match an actual plugin name.
    // A unique userId is generated by appSetup if not provided.
    const setupData = await globalUtils.appSetup({
      appName: 'lockTestPlugin',    // This will be the integrationId and plugin name
      token: shortTTRTokenConfig,   // Pass the specific token config for UserAppKey
      tokenHint: ttrTestHint,       // Pass the specific hint for UserAppKey
    });

    testAppName = setupData.newApp.name; // Should be 'lockTestPlugin'
    testUserId = setupData.userId;
    userToken = globalUtils.createUserToken(testUserId); // JWT for API calls

    // The UserAppKey with shortTTRTokenConfig and ttrTestHint is now created by appSetup.
    // No need for an additional doApiPost to create/update it here.

    // Initial cache clear for this specific context. testAppName is 'lockTestPlugin'.
    const cacheKey = hash({
      userId: testUserId,
      hint: ttrTestHint,
      operationId: 'bookingsProductSearch',
    });
    await cache.drop({ pluginName: testAppName, key: cacheKey });
    await cache.drop({ pluginName: testAppName, key: `${cacheKey}:lastUpdated` });
    await cache.drop({ pluginName: testAppName, key: `${cacheKey}:lock` }); // pluginExecutionLock
    await cache.drop({ pluginName: testAppName, key: `${cacheKey}:jobLock` }); // jobQueueLock
  });

  const clearProductSearchCache = async () => {
    const cacheKey = hash({
      userId: testUserId,
      hint: ttrTestHint,
      operationId: 'bookingsProductSearch',
    });
    await cache.drop({ pluginName: testAppName, key: cacheKey });
    await cache.drop({ pluginName: testAppName, key: `${cacheKey}:lastUpdated` });
    await cache.drop({ pluginName: testAppName, key: `${cacheKey}:lock` });
    await cache.drop({ pluginName: testAppName, key: `${cacheKey}:jobLock` });
  };

  const makeProductSearchRequest = () => doApiPost({
    url: `/products/${testAppName}/${testUserId}/${ttrTestHint}/search`,
    token: userToken,
    payload: {},
  });

  beforeEach(async () => {
    // Clear mocks before each test in this suite
    addJob.mockClear(); // Reset addJob mock calls
    if (lockTestPlugin && lockTestPlugin.searchProducts && lockTestPlugin.searchProducts.mockClear) {
      lockTestPlugin.searchProducts.mockClear();
    }
    // Clear other global mocks if they were used by this suite and need resetting
    if (mockJobStatus && mockJobStatus.mockClear) mockJobStatus.mockClear();
  });

  it('multiple concurrent requests to stale cache should serve stale data and queue only one new refresh job', async () => {
    // 1. First call: Populate the cache using the specific lockTestPlugin
    lockTestPlugin.searchProducts.mockResolvedValueOnce({ products: [{ id: 'prod1', name: 'Initial Product' }] });

    await doApiPost({
      url: `/products/${testAppName}/${testUserId}/${ttrTestHint}/search`, // testAppName is 'lockTestApp'
      token: userToken,
      payload: {},
    });
    expect(lockTestPlugin.searchProducts).toHaveBeenCalledTimes(1);
    expect(addJob).not.toHaveBeenCalled(); // No job queued on initial population
    lockTestPlugin.searchProducts.mockClear(); // Clear for next phase

    // 2. Wait for TTR to expire (ttlForProducts is 1s, wait 1.5s)
    await new Promise(resolve => setTimeout(resolve, 1500));

    // 3. Make multiple concurrent requests to the now stale cache
    // Mock plugin response for the background refresh. This mock is for the job that addJob is supposed to queue.
    lockTestPlugin.searchProducts.mockResolvedValueOnce({ products: [{ id: 'prod2', name: 'Refreshed Product' }] });

    const makeRequest = () => doApiPost({
      url: `/products/${testAppName}/${testUserId}/${ttrTestHint}/search`,
      token: userToken,
      payload: {},
    });

    const requestPromises = [];
    requestPromises.push(makeRequest()); // First request hits stale cache, should queue job
    await global.sleep(50); // Small delay to simulate near concurrency
    requestPromises.push(makeRequest()); // Second request should also hit stale cache, but not queue another job
    await global.sleep(50);
    requestPromises.push(makeRequest()); // Third request

    const results = await Promise.all(requestPromises);

    // Assertions:
    // a. All requests should serve stale data (the "Initial Product")
    results.forEach(result => {
      expect(result.products).toEqual([{ id: 'prod1', name: 'Initial Product' }]);
    });

    // b. The plugin's searchProducts method (on lockTestPlugin) should NOT have been called directly by these API requests
    expect(lockTestPlugin.searchProducts).not.toHaveBeenCalled();

    // c. addJob should have been called exactly once
    expect(addJob).toHaveBeenCalledTimes(1);

    // d. Verify arguments of addJob
    if (addJob.mock.calls.length > 0) {
      const expectedPluginMethodName = lockTestPlugin.searchProducts ? 'searchProducts' : 'searchProductsForItinerary';
      expect(addJob).toHaveBeenCalledWith(
        expect.objectContaining({
          type: 'plugin',
          pluginName: testAppName, // This should be 'lockTestPlugin'
          method: expectedPluginMethodName,
          token: expect.objectContaining({ ttlForProducts: shortTTRTokenConfig.ttlForProducts }),
          payload: expect.objectContaining({
            payload: { forceRefresh: false }, // originalRequestBody was empty for the product search
            userId: testUserId,
          }),
          postProcess: expect.objectContaining({
            controller: 'bookings',
            action: '$updateProductSearchCache',
            args: expect.objectContaining({
              appKey: testAppName, // 'lockTestPlugin'
              userId: testUserId,
              hint: ttrTestHint,
            }),
          }),
        }),
        { removeOnComplete: true }
      );
    }
  });

  it('multiple concurrent requests with no cache should wait for one plugin fetch', async () => {
    await clearProductSearchCache();

    const coldProducts = [{
      productId: 'cold-product',
      productName: 'Cold Product',
      options: [{
        optionId: 'cold-option',
        optionName: 'Cold Option',
      }],
    }];
    lockTestPlugin.searchProducts.mockReset();
    const releasePluginFetches = [];
    const pluginFetchStarted = new Promise(resolve => {
      lockTestPlugin.searchProducts.mockImplementation(() => {
        resolve();
        return new Promise(pluginResolve => {
          releasePluginFetches.push(() => pluginResolve({ products: coldProducts }));
        });
      });
    });

    const requestPromises = [makeProductSearchRequest(), makeProductSearchRequest(), makeProductSearchRequest()];
    await pluginFetchStarted;
    await new Promise(resolve => setTimeout(resolve, 50));

    releasePluginFetches.forEach(releasePluginFetch => releasePluginFetch());
    const results = await Promise.all(requestPromises);

    results.forEach(result => {
      expect(result.products).toEqual(coldProducts);
    });
    expect(lockTestPlugin.searchProducts).toHaveBeenCalledTimes(1);
    expect(addJob).not.toHaveBeenCalled();
  });

  it('concurrent forceRefresh requests should wait for one plugin fetch', async () => {
    await clearProductSearchCache();

    const refreshedProducts = [{
      productId: 'force-product',
      productName: 'Force Product',
      options: [{
        optionId: 'force-option',
        optionName: 'Force Option',
      }],
    }];
    lockTestPlugin.searchProducts.mockReset();
    const releasePluginFetches = [];
    const pluginFetchStarted = new Promise(resolve => {
      lockTestPlugin.searchProducts.mockImplementation(() => {
        resolve();
        return new Promise(pluginResolve => {
          releasePluginFetches.push(() => pluginResolve({ products: refreshedProducts }));
        });
      });
    });
    const makeForceRequest = () => doApiPost({
      url: `/products/${testAppName}/${testUserId}/${ttrTestHint}/search`,
      token: userToken,
      payload: { forceRefresh: true },
    });

    const requestPromises = [makeForceRequest(), makeForceRequest(), makeForceRequest()];
    await pluginFetchStarted;
    await new Promise(resolve => setTimeout(resolve, 50));

    releasePluginFetches.forEach(releasePluginFetch => releasePluginFetch());
    const results = await Promise.all(requestPromises);

    results.forEach(result => {
      expect(result.products).toEqual(refreshedProducts);
    });
    expect(lockTestPlugin.searchProducts).toHaveBeenCalledTimes(1);
    expect(addJob).not.toHaveBeenCalled();
  });

  it('renews the plugin execution lock while a cold product fetch exceeds the initial TTL', async () => {
    await clearProductSearchCache();
    const originalLockTtl = process.env.PRODUCT_SEARCH_LOCK_TTL_SECONDS;
    process.env.PRODUCT_SEARCH_LOCK_TTL_SECONDS = '1';

    const slowProducts = [{
      productId: 'slow-product',
      productName: 'Slow Product',
      options: [{
        optionId: 'slow-option',
        optionName: 'Slow Option',
      }],
    }];
    lockTestPlugin.searchProducts.mockReset();
    const expireIfValueSpy = jest.spyOn(lockTestPlugin.cache, 'expireIfValue');
    const releasePluginFetches = [];
    const pluginFetchStarted = new Promise(resolve => {
      lockTestPlugin.searchProducts.mockImplementation(() => {
        resolve();
        return new Promise(pluginResolve => {
          releasePluginFetches.push(() => pluginResolve({ products: slowProducts }));
        });
      });
    });

    try {
      const firstRequest = makeProductSearchRequest();
      await pluginFetchStarted;
      await new Promise(resolve => setTimeout(resolve, 1500));
      const secondRequest = makeProductSearchRequest();
      await new Promise(resolve => setTimeout(resolve, 50));

      releasePluginFetches.forEach(releasePluginFetch => releasePluginFetch());
      const results = await Promise.all([firstRequest, secondRequest]);

      results.forEach(result => {
        expect(result.products).toEqual(slowProducts);
      });
      expect(lockTestPlugin.searchProducts).toHaveBeenCalledTimes(1);
      expect(addJob).not.toHaveBeenCalled();
      expect(expireIfValueSpy).toHaveBeenCalled();
    } finally {
      expireIfValueSpy.mockRestore();
      if (originalLockTtl === undefined) {
        delete process.env.PRODUCT_SEARCH_LOCK_TTL_SECONDS;
      } else {
        process.env.PRODUCT_SEARCH_LOCK_TTL_SECONDS = originalLockTtl;
      }
    }
  });

  it('multiple concurrent requests with no cache should share an empty plugin result', async () => {
    await clearProductSearchCache();

    lockTestPlugin.searchProducts.mockReset();
    const releasePluginFetches = [];
    const pluginFetchStarted = new Promise(resolve => {
      lockTestPlugin.searchProducts.mockImplementation(() => {
        resolve();
        return new Promise(pluginResolve => {
          releasePluginFetches.push(() => pluginResolve({ products: [] }));
        });
      });
    });

    const requestPromises = [makeProductSearchRequest(), makeProductSearchRequest(), makeProductSearchRequest()];
    await pluginFetchStarted;
    await new Promise(resolve => setTimeout(resolve, 50));

    releasePluginFetches.forEach(releasePluginFetch => releasePluginFetch());
    const results = await Promise.all(requestPromises);

    results.forEach(result => {
      expect(result.products).toEqual([]);
    });
    expect(lockTestPlugin.searchProducts).toHaveBeenCalledTimes(1);
    expect(addJob).not.toHaveBeenCalled();
  });

  it('multiple concurrent requests with no cache should fail when the leader throws', async () => {
    await clearProductSearchCache();

    lockTestPlugin.searchProducts.mockReset();
    let rejectPluginFetch;
    const pluginFetchStarted = new Promise(resolve => {
      lockTestPlugin.searchProducts.mockImplementation(() => {
        resolve();
        return new Promise((_, reject) => {
          rejectPluginFetch = () => reject(new Error('boom'));
        });
      });
    });

    const requestPromises = [makeProductSearchRequest(), makeProductSearchRequest(), makeProductSearchRequest()]
      .map(requestPromise => requestPromise
        .then(result => ({ status: 'fulfilled', result }))
        .catch(error => ({ status: 'rejected', error })));
    await pluginFetchStarted;
    await new Promise(resolve => setTimeout(resolve, 50));

    rejectPluginFetch();
    const results = await Promise.all(requestPromises);

    results.forEach(result => {
      expect(result.status).toBe('rejected');
    });
    expect(lockTestPlugin.searchProducts).toHaveBeenCalledTimes(1);
    expect(addJob).not.toHaveBeenCalled();

    const statuses = results.map(result => result.error.response.status).sort();
    expect(statuses).toEqual([500, 503, 503]);
  });
});
